public class ArithmaticOp{
	public static void main(String ar[]){
		int n1 = 4;
		int n2 = 2;
		System.out.println(n1+ " + "+n2+ " = " + (n1+n2));
		System.out.println(n1+ " - "+n2+ " = " + (n1-n2));
		System.out.println(n1+ " x "+n2+ " = " + n1*n2);
		System.out.println(n1+ " / "+n2+ " = " + n1/n2);
	}
}