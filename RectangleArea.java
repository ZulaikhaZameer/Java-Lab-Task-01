public class RectangleArea{
	public static void main(String ar[]){
		float width = 3;
		float length = 5;
		
		System.out.println("For rectangle "+width+" inches wides and "+length+" inches long: ");
		System.out.println("Area : "+ width * length + " sq. inches");
		System.out.println("Circumference: "+ (2 * (width+length))+ " inches");
		
		width = 6.8f;
		length = 3.3f;
		
		System.out.println("For rectangle "+width+" inches wides and "+length+" inches long: ");
		System.out.println("Area : "+ width * length + " sq. inches");
		System.out.println("Circumference: "+ (2 * (width+length))+ " inches");
	}	
}
